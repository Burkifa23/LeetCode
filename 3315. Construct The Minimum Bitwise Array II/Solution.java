import java.util.List;
class Solution {
    public int[] minBitwiseArray(List<Integer> nums) {
        int n = nums.size();
        int[] ans = new int[n];

        for ( int i=0;i<n;i++) {
            int p = nums.get(i);

            if(p==2){
                ans[i]=-1;
            } else {
                int trailingOnes = Integer.numberOfTrailingZeros(~p);
                ans[i] = p-(1<<(trailingOnes-1));
            }
        }
        return ans;
    }
}
